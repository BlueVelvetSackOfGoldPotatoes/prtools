%GETPOSTPROC Get datafile POSTPROC field
%
%	   W = GETPOSTPROC(A)
%
% Retrieves the POSTPROC field of the datafile A. This is a
% mapping that is executed by the READDATAFILE command before
% it returns a dataset. It is thereby also executed when a
% datafile is converted into a dataset.

function w = getpostproc(a)

	prtrace(mfilename,2);
  
  w = a.postproc;
  
return
